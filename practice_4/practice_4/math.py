import math
import random

# Example 1: Built-in functions
print(min(3, 7, 1))
print(max(3, 7, 1))
print(abs(-8))
print(round(3.6))

# Example 2: Power
print(pow(2, 3))

# Example 3: Math module
print(math.sqrt(25))
print(math.ceil(4.2))
print(math.floor(4.8))

# Example 4: Constants
print(math.pi)
print(math.e)

# Example 5: Random
print(random.random())
print(random.randint(1, 10))

a = [1, 2, 3, 4]
random.shuffle(a)
print(a)
